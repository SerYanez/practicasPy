primer_numero = int(input("Ingrese un número: "))
segundo_numero = int(input("Ingrese otro número: "))

suma = int(primer_numero + segundo_numero)
resta = int(primer_numero - segundo_numero)
division = float(primer_numero / segundo_numero)
division_entera = primer_numero // segundo_numero
modulo = primer_numero % segundo_numero
potencia = primer_numero ** segundo_numero

print(f"La suma de ambos números es: {suma}")
print(f"La resta de ambos números es: {resta}")
print(f"La división de ambos números es: {division}")
print(f"La división entera de ambos números es: {division_entera}")
print(f"El módulo de ambos números es: {modulo}")
print(f"El resultado de ambos números potenciados es: {potencia}")
